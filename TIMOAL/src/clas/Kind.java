package clas;

public class Kind {
private int n;
private int a;
private int b;
public Kind(){}

public int getN() {
	return n;
}

public void setN(int n) {
	this.n = n;
}

public int getA() {
	return a;
}
public void setA(int a) {
	this.a = a;
}
public int getB() {
	return b;
}
public void setB(int b) {
	this.b = b;
}
}
